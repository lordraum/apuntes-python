# python es case sensitive
# Las variables se declaran con el nombre de la variable y el operador =
# No se debe denominar las variables iniciando con un número

name = "David"
print(name)

# Declarar más de una variable en una línea

number, book = 5, '100 años de soledad'

print(number, book)

# Por convención se utilizan mayúsculas para las variables que no queremos que cambien (constantes, aunque no es estricto)

PI = 3.1416
MY_CC = 1025365689

print(PI,MY_CC)

# python es de tipado dinámico, las variables pueden ser reasignadas y pueden almacenar cualquier tipo de dato

age = 38
print(age)
age = '38 años'
print(age)






